# include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	size_t			i;
	int				r;

	i = 0;
	r = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			r = s1[i] - s2[i];
			if (r > 0)
			{
				return (1);
			}
			return (-1);
		}
		i++;
	}
	return (r);
}